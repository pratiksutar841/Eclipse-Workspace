/*  type conversion or type casting
 types:-
 1.implicit Conversion or widening(Done automatic by compiler)
 2.Explicit conversion or Narrowing(Done forcefully by programmer)
  
 */
package CoreJava;
public class Typecast 
{
 	public static void main(String[] args) 
 	{
      int a=10;
      double d;
      d=a;
      System.out.println(d);
      float e;
      byte f=20;
      e=a;
      a=f;
      System.out.println(f);

	}

}
